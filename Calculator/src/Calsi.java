
public class Calsi {
	
	public int Addition(int a,int b) 
	{
		return a+b;
	}
	
	public int Subraction(int a, int b)
	{
		return a-b;
	}
	public int Multiplication(int a, int b)
	{
		return a*b;
	}
	public int Division(int a, int b) 
	{
		return a/b;
	}
	public double Percentage(double a,double b)
	
	{
		return (a/b)*100;
		
	}
}
